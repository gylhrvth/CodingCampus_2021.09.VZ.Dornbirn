package Oemer;

public class Tag3Schleifen {
    public static void main(String[] args) {

        for (int i = 0; i <= 10; i++) {
            System.out.println(i * 5);
        }

        int k = 0;
        while (k <= 10) {
            System.out.println(k * 5);
            k++;
        }
        for (int x = 0; x <= 5; x++) {
            for (int p = 0; p <= 4; p++) {
                System.out.print("x");
            }
            System.out.println();
        }

    }


}
