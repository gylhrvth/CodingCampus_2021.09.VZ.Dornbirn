public class Aufgabe1 {
    public static void main(String[] args) {
        //i = i + 1
        for (int i = 1; i <= 10; i++) {
            System.out.println(i * 5);
        }

        //i = i + 5
        for (int i = 5; i <= 50; i += 5) {
            System.out.println(i);
        }

        int j = 10;
        j = -5;
        System.out.println(j);
    }
}
