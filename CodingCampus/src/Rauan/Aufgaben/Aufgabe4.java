package Rauan.Aufgaben;

public class Aufgabe4 {
    public static void main(String[] args) {
        //Datentypen : byte, short, int, long, float, double
        //probiere aus die folgenden operatoren:
        // + Addition, -Substraktion, *MUltiplikation, / Dividieren, % Moulo

        byte nummer1 = 8;
        byte nummer2 = 9;

        short nummer3 = 2;
        short nummer4 = 3;

        int nummer5 = 4;
        int nummer6 = 5;

        long nummer7 = 23543664L;
        long nummer8 = 32435627L;

        float nummer9 = 1.2F;
        float nummer10 = 3.4F;

        double nummer11 = 45.67;
        double nummer12 = 33.21;


        //Rechnungen
        // byte
        System.out.println(nummer1 + nummer2);
        System.out.println(nummer1 - nummer2);
        System.out.println(nummer1 * nummer2);
        System.out.println(nummer1 / nummer2);
        System.out.println(nummer1 % nummer2);

        //short
        System.out.println(nummer3 + nummer4);
        System.out.println(nummer3 - nummer4);
        System.out.println(nummer3 * nummer4);
        System.out.println(nummer3 / nummer4);
        System.out.println(nummer3 % nummer4);

        //int
        System.out.println(nummer5 + nummer6);
        System.out.println(nummer5 - nummer6);
        System.out.println(nummer5 * nummer6);
        System.out.println(nummer5 / nummer6);
        System.out.println(nummer5 % nummer6);

        //long
        System.out.println(nummer7 + nummer8);
        System.out.println(nummer7 - nummer8);
        System.out.println(nummer7 * nummer8);
        System.out.println(nummer7 / nummer8);
        System.out.println(nummer7 % nummer8);

        //float
        System.out.println(nummer9 + nummer10);
        System.out.println(nummer9 - nummer10);
        System.out.println(nummer9 * nummer10);
        System.out.println(nummer9 / nummer10);
        System.out.println(nummer9 % nummer10);

        //double
        System.out.println(nummer11 + nummer12);
        System.out.println(nummer11 - nummer12);
        System.out.println(nummer11 * nummer12);
        System.out.println(nummer11 / nummer12);
        System.out.println(nummer11 % nummer12);
    }
}
