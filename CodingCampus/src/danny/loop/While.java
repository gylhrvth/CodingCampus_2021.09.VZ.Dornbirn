package danny.loop;

public class While {
    public static void main(String[] args) {

        // AufgabeWhile


        for (int x = 0; x < 5; x++) {
            for (int r = 0; r < x; r++) {
                System.out.print("*");

            }

            System.out.println();

        }
        for (int c = 5; c > 0; c--) {
            for (int s = 0; s < c; s++) {
                System.out.print("*");

            }

            System.out.println();


        }
    }
}







